#Calcular el sueldo de un empleado tomando las horas trabajadas por el valor hora
horas_trabajadas = int(input("Ingrese las horas trabajadas "))
valor_hora = int(input("Ingrese el valor de la hora de trabajo "))
sueldo = horas_trabajadas * valor_hora
print("El sueldo de un empleado es de $" , sueldo)