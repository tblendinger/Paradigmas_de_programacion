n1 = int(input("Ingrese el primer numero "))
n2 = int(input("Ingrese el segundo numero "))
if n1 > n2: 
    print(n1 , " + " , n2 , " = " , n1 + n2)
    print(n1 , " - " , n2 , " = " , n1 - n2)
else:
    print(n1 , " / " , n2 , " = " , n1 / n2)
    print(n1 , " // " , n2 , " = " , n1 // n2)
    print(n1 , " * " , n2 , " = " , n1 * n2)